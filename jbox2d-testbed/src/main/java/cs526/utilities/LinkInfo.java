package cs526.utilities;

/**
 * Information used to represent a link
 * @author shuo
 *
 */
public class LinkInfo {
	/**
	 * height of the link (the smaller side in m)
	 */
	public float height;	
	
	/**
	 * width of the link (the larger side in m)
	 */
	public float width;
	
	/**
	 * mass of the link (in kg)
	 */
	public float mass;
}